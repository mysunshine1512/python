num_day = int(input("Nhập số ngày trong tuần: "))
if num_day == 1 :
    print("Monday")
elif num_day == 2 :
    print("Tuesday")
elif num_day == 3 :
    print("Wednesday")
elif num_day == 4 :
    print("Thursday")
elif num_day == 5 :
    print("Friday")
elif num_day == 6 :
    print("Saturday")
elif num_day == 7 :
    print("Sunday")
else :
    print("Nằm ngoài số quy định")